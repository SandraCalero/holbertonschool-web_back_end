#!/usr/bin/env python3
"""
"Returning the concatenation of str1 and str2."
The first line of the docstring is a one sentence summary of the function
:param str1: str
:type str1: str
:param str2: str
:type str2: str
:return: The concatenation of str1 and str2.
"""


def concat(str1: str, str2: str) -> str:
    """Returning the concatenation of str1 and str2."""
    return str1 + str2
